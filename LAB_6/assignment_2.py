# Ввод данных
integer_input = int(input("Введите целое число: "))
float_input = float(input("Введите число с плавающей точкой: "))
string_input = input("Введите строку: ")

# Вывод типов
print("Тип целого числа:", type(integer_input))
print("Тип числа с плавающей точкой:", type(float_input))
print("Тип строки:", type(string_input))